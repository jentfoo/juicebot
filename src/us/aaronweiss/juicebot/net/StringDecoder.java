package us.aaronweiss.juicebot.net;

import org.jboss.netty.buffer.ChannelBuffer;
import org.jboss.netty.channel.Channel;
import org.jboss.netty.channel.ChannelHandlerContext;
import org.jboss.netty.handler.codec.oneone.OneToOneDecoder;

/**
 * A basic decoder to convert <code>ChannelBuffers</code> to strings.
 * @author Aaron Weiss
 * @version 1.0
 * @since 1.0
 */
public class StringDecoder extends OneToOneDecoder {
	@Override
	protected Object decode(ChannelHandlerContext ctx, Channel channel, Object message) throws Exception {
		ChannelBuffer buffer = (ChannelBuffer) message;
		String ret = new String(buffer.array());
		return (ret.contains("\r\n")) ? ret : null;
	}
}
